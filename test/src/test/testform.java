    private void tfIDKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfIDKeyTyped
        char c = evt.getKeyChar();
        if(!Character.isDigit((c))){
            evt.consume();
        }
    }//GEN-LAST:event_tfIDKeyTyped

    private void tfEmailFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_tfEmailFocusLost
        if (!(Pattern.matches("^[a-zA-Z0-9]+[@]{1}+[a-zA-Z0-9]+[.]{1}+[a-zA-Z0-9]+$", jEmail.getText())))
        {
            JOptionPane.showMessageDialog(null, "Por favor, digite um e-mail v√°lido", "Error", JOptionPane.ERROR_MESSAGE);
            jEmail.requestFocus();
        }
    }//GEN-LAST:event_tfEmailFocusLost

    private void tfNomeKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfNomeKeyTyped
        char c = evt.getKeyChar();
        if(!Character.isAlphabetic((c))){
            evt.consume();
        }
    }//GEN-LAST:event_tfNomeKeyTyped

